let average_marks = 85;

if (average_marks >= 90) {
    console.log('Excellent');
} 
else if (average_marks >= 80) {
    console.log("Good job!");
} 
else if (average_marks >= 70) {
    console.log("Needs improvement.");
} 
else if (average_marks >= 60) {
    console.log("Passing, could do better.");
} 
else {
    console.log("Fail");
}
